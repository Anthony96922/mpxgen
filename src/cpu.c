#define _GNU_SOURCE
#include <stdio.h>
#include <sched.h>

void set_affinity(int cpu) {
	cpu_set_t mask;
	CPU_ZERO(&mask);
	CPU_SET(cpu, &mask);
	if (sched_setaffinity(0, sizeof(mask), &mask) == -1) {
		fprintf(stderr, "Warning: Could not set CPU affinity\n");
	}
}
